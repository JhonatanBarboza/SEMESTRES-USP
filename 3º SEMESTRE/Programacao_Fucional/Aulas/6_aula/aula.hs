-- programação funcional caracteristicas:

-- 1. Preguiça : ela so calcula o que é necessário
-- 2. Função de primeira classe : funções podem ser passadas como argumentos
-- 3. Funções de alta ordem : funções que recebem funções como argumentos ou retornam funções
-- 4. Função pura : não tem efeitos colaterais, não altera o estado do programa, no sentido matematico 
-- 5. Recursão : não tem laços, não tem for, while, do while, etc
-- 6. Transparencia referencial : se uma função é pura, ela pode ser substituida por seu resultado

